	{
		generate_quantities_classic$save_output_files(dir = tree_path, basename = paste0("GQ-run=", run), timestamp = FALSE, random = TRUE)
		generate_quantities_classic$save_object(file = savingGQ_file)
	}

	return (list(generate_quantities_ssm = generate_quantities_ssm, generate_quantities_classic = generate_quantities_classic,
		stanData_ssm = stanData_ssm, stanData_classic = stanData_classic))
}

## Function to compute and plot residuals from fit
residuals_fit = function(species, run, filenamePattern = NULL)
{
	tree_path = paste0("./", species, "/")

	filenameResiduals = paste0(tree_path, run, "_avg_residuals_ssmFit.rds")
	if (file.exists(filenameResiduals))
	{
		residuals_load = readRDS(filenameResiduals)
		warning("Plot was bypass")
		return (residuals_load)
	}

	info_lastRun = getLastRun(path = tree_path, begin = "^growth-", extension = "_main.rds$", format = "ymd", run = run, hour = TRUE)
	ssm = readRDS(paste0(tree_path, info_lastRun[["file"]]))

	info_lastRun = getLastRun(path = tree_path, begin = "^growth-", extension = "_classic.rds$", format = "ymd", run = run, hour = TRUE)
	classic = readRDS(paste0(tree_path, info_lastRun[["file"]]))

	n_chains = ssm$num_chains()

	latent_G_ssm = ssm$draws("latent_growth")
	n_iter = dim(latent_G_ssm)[1]

	latent_G_classic = classic$draws("latent_avg_annual_growth")

	indices = readRDS(paste0(tree_path, run, "_indices.rds"))[["indices"]]
	tree_data = readRDS(paste0(tree_path, run, "_treeData.rds"))
	n_indiv = unique(indices[, .(tree_id, plot_id)])[, .N]
	indices[.N, index_gen] - n_indiv == dim(latent_G_ssm)[3]

	growth_dt = na.omit(tree_data)

	sd_dbh = readRDS(paste0(tree_path, run, "_dbh_normalisation.rds"))[variable == "dbh", sd]

	start = 1

	residuals_ssm = posterior::as_draws_array(array(data = NA, dim = c(n_iter, n_chains, growth_dt[, .N])))
	reconstructedGrowth_ssm = posterior::as_draws_array(array(data = NA, dim = c(n_iter, n_chains, growth_dt[, .N])))
	residuals_classic = posterior::as_draws_array(array(data = NA, dim = c(n_iter, n_chains, growth_dt[, .N])))

	for (g in seq_len(growth_dt[, .N]))
	{
		nbYears = growth_dt[g, deltaYear]
		end = start + nbYears - 1

		reconstructedGrowth_ssm[, , g] = sd_dbh *
			posterior::as_draws_array(array(rowSums(latent_G_ssm[, , start:end], dim = 2), dim = c(n_iter, n_chains, 1)))/nbYears

		residuals_ssm[, , g] = sd_dbh *
			posterior::as_draws_array(array(rowSums(latent_G_ssm[, , start:end], dim = 2), dim = c(n_iter, n_chains, 1)))/nbYears -
			growth_dt[g, growth]

		residuals_classic[, , g] = sd_dbh * latent_G_classic[, , g] - growth_dt[g, growth]

		start = end + 1
		if (g %% 200 == 0)
			print(paste0(round(g*100/growth_dt[, .N], 2), "% done"))
	}

	avg_residuals_ssmFit = apply(X = residuals_ssm, MARGIN = 3, FUN = mean)
	avg_residuals_classicFit = apply(X = residuals_classic, MARGIN = 3, FUN = mean)

	ind = which(growth_dt[, growth] >= -0.5 & growth_dt[, growth] < 12)

	if (!is.null(filenamePattern))
		pdf(paste0(tree_path, run, filenamePattern))
	
	plot(growth_dt[ind, growth], avg_residuals_ssmFit[ind], pch = 19, col = "#AABBCCBB")
	points(growth_dt[ind, growth], avg_residuals_classicFit[ind], pch = 19, col = "#11BBCC11")
	abline(h = 0, lwd = 3, col = "#CD2A1A")
	abline(v = 0, lwd = 3, col = "#CD2A1A")

	if (!is.null(filenamePattern))
		dev.off()

	saveRDS(list(residuals_ssm = avg_residuals_ssmFit, residuals_classic = avg_residuals_classicFit,
		reconstructedGrowth_ssm = reconstructedGrowth_ssm), filenameResiduals)

	return (list(residuals_ssm = avg_residuals_ssmFit, residuals_classic = avg_residuals_classicFit,
		reconstructedGrowth_ssm = reconstructedGrowth_ssm))
}

## Function to compute, extract, and save climatic quantiles on rasters
climQuantiles = function(clim_var, years, coords, clim_folder = "/bigdata/Predictors/climateChelsa/", probs = c(0.05, 0.25, 0.75, 0.95))
{
	# Check-up
	if (!dir.exists(clim_folder))
		stop(paste0("Folder <", clim_folder, "> does not exist"))

	ls_vars = list.dirs(path = clim_folder, full.names = FALSE, recursive = FALSE)

	if (!(clim_var %in% ls_vars))
		stop(paste0("Climatic variable <", clim_var, "> not found in folder<", clim_folder, ">"))

	folder = paste0(clim_folder, clim_var, "/")

	ls_years = dir(path = folder, pattern = ".tif$")
	ls_years = stri_sub(str = ls_years, to = stri_locate(str = ls_years, regex = ".tif")[, "start"] - 1)

	if (!all(years %in% ls_years))
	{
		years = years[years %in% ls_years]
		warning("Some years were missing, they have been removed")
	}

	# Loading rasters
	ls_files = paste0(folder, years, ".tif")

	clim = rast(ls_files)

	clim_qt = quantile(clim, probs = probs)
	coords_rs = vect(x = coords, geom = c("x", "y"), crs = "EPSG:4326")

	if (crs(clim_qt, describe = TRUE)$code != crs(coords_rs, describe = TRUE)$code)
		stop("Projections vect differ")

	values_qt = setDT(extract(x = clim_qt, y = coords_rs))
	values_qt[, ID := NULL]
	values_qt[, plot_id := coords_rs[["plot_id"]]]
	setnames(values_qt, new = stri_replace_all(str = names(values_qt), replacement = "", regex = "0\\."))

	return(values_qt)
}

## Function to compute growth time series
growth_timeSeries = function(species, run, selected_plot_id, init_dbh, nbYearsGrowth_new, year_start_indiv, caption = TRUE, extension = NULL)
{
	tree_path = paste0("./", species, "/")

	# Load results
	# --- State-Space Model approach
	info_lastRun = getLastRun(path = tree_path, begin = "growth-", extension = "_main.rds$", run = run)
	lastRun = info_lastRun[["file"]]
	ssm = readRDS(paste0(tree_path, lastRun))

	# --- Classic approach
	info_lastRun = getLastRun(path = tree_path, begin = "growth-", extension = "_classic.rds$", run = run)
	lastRun = info_lastRun[["file"]]
	classic = readRDS(paste0(tree_path, lastRun))

	n_chains = ssm$num_chains() # Same for classic approach

	# Prepare climate
	dataEnv_ls = getEnvSeries(species, run)
	precipitations = dataEnv_ls[["dataEnv"]][selected_plot_id, pr, by = .EACHI][, pr]
	temperatures = dataEnv_ls[["dataEnv"]][selected_plot_id, tas, by = .EACHI][, tas]
	ph = dataEnv_ls[["dataEnv"]][selected_plot_id, ph, by = .EACHI][, ph]
	basalAreas = dataEnv_ls[["dataEnv"]][selected_plot_id, standBasalArea_interp, by = .EACHI][, standBasalArea_interp]

	if (length(temperatures) != sum(nbYearsGrowth_new) + length(init_dbh))
		stop("Dimensions mismatch between climate and number of latent growth, individuals")

	# Extract common time-span for all the trees
	time_span = dataEnv_ls[["dataEnv"]][unique(selected_plot_id), min(year), by = plot_id]
	setnames(x = time_span, old = "V1", new = "minYear")
	time_span[, maxYear := dataEnv_ls[["dataEnv"]][unique(selected_plot_id), max(year), by = plot_id][, V1]]

	year_start = max(time_span[, minYear]) # Maximum starting year (for both dbh and growth)
	year_end = min(time_span[, maxYear]) # Minimum ending year (for dbh only)
	year_end_growth = year_end - 1 # Minimum ending year (for growth only)

	# Prepare new stan data
	# --- Load stan data
	stanData_ssm = readRDS(paste0(tree_path, run, "_stanData.rds"))
	stanData_classic = readRDS(paste0(tree_path, run, "_stanData_classic.rds"))

	# --- Add the new environment (x_r)
	stanData_ssm$x_r = c((precipitations - dataEnv_ls[["scaling_clim_ssm"]]["pr", mu])/dataEnv_ls[["scaling_clim_ssm"]]["pr", sd],
		(temperatures - dataEnv_ls[["scaling_clim_ssm"]]["tas", mu])/dataEnv_ls[["scaling_clim_ssm"]]["tas", sd],
		(ph - dataEnv_ls[["scaling_ph_ssm"]]["ph", mu])/dataEnv_ls[["scaling_ph_ssm"]]["ph", sd],
		(basalAreas - dataEnv_ls[["scaling_ba_ssm"]]["standBasalArea_interp", mu])/
			dataEnv_ls[["scaling_ba_ssm"]]["standBasalArea_interp", sd])

	precipitations = (precipitations - dataEnv_ls[["scaling_clim_classic"]]["pr_avg", mu])/dataEnv_ls[["scaling_clim_classic"]]["pr_avg", sd]
	temperatures = (temperatures - dataEnv_ls[["scaling_clim_classic"]]["tas_avg", mu])/dataEnv_ls[["scaling_clim_classic"]]["tas_avg", sd]
	ph = (ph - dataEnv_ls[["scaling_ph_classic"]]["ph", mu])/dataEnv_ls[["scaling_ph_classic"]]["ph", sd]
	basalAreas = (basalAreas - dataEnv_ls[["scaling_ba_classic"]]["standBasalArea_interp_avg", mu])/
		dataEnv_ls[["scaling_ba_classic"]]["standBasalArea_interp_avg", sd]

	stanData_classic$x_r = c(precipitations, temperatures, ph, basalAreas)

	x_r_avg = dataEnv_ls[["dataEnv"]][unique(selected_plot_id), lapply(.SD, mean), .SDcols = c("pr", "tas", "ph", "standBasalArea_interp"),
		by = plot_id]
	x_r_avg = x_r_avg[rep(x = x_r_avg[, .I], times = as.numeric(table(selected_plot_id)))] # selected_plot_id is in the good order
	
	x_r_avg[, pr := (pr - dataEnv_ls[["scaling_clim_classic"]]["pr_avg", mu])/dataEnv_ls[["scaling_clim_classic"]]["pr_avg", sd]]
	x_r_avg[, tas := (tas - dataEnv_ls[["scaling_clim_classic"]]["tas_avg", mu])/dataEnv_ls[["scaling_clim_classic"]]["tas_avg", sd]]
	x_r_avg[, ph := (ph - dataEnv_ls[["scaling_ph_classic"]]["ph", mu])/dataEnv_ls[["scaling_ph_classic"]]["ph", sd]]
	x_r_avg[, standBasalArea_interp := (standBasalArea_interp - dataEnv_ls[["scaling_ba_classic"]]["standBasalArea_interp_avg", mu])/
		dataEnv_ls[["scaling_ba_classic"]]["standBasalArea_interp_avg", sd]]
	
	stanData_classic$x_r_avg = x_r_avg[, .(pr, tas, ph, standBasalArea_interp)]

	# --- Add the new dimensions
	stanData_ssm$n_latent_dbh_new = length(temperatures) # Climate 'aligned' with latent dbh
	stanData_ssm$n_indiv_new = length(init_dbh)
	stanData_ssm$dbh0 = init_dbh
	stanData_ssm$n_growing_years_new = stanData_ssm$n_latent_dbh_new - stanData_ssm$n_indiv_new
	stanData_ssm$nbYearsGrowth_new = nbYearsGrowth_new
	stanData_ssm$n_growth = stanData_ssm$n_children

	stanData_classic$n_latent_dbh_new = length(temperatures)
	stanData_classic$n_indiv_new = length(init_dbh)
	stanData_classic$dbh0 = init_dbh
	stanData_classic$n_growing_years_new = stanData_classic$n_latent_dbh_new - stanData_classic$n_indiv_new
	stanData_classic$nbYearsGrowth_new = nbYearsGrowth_new

	# Generate quantities
	# --- Stan models to simulate growth
	gq_model_ssm = cmdstan_model("generate_posterior.stan")
	gq_model_classic = cmdstan_model("generate_posterior_classic.stan")

	# --- Run simulations
	generate_quantities_ssm = gq_model_ssm$generate_quantities(ssm$draws(), data = stanData_ssm, parallel_chains = n_chains)
	generate_quantities_classic = gq_model_classic$generate_quantities(classic$draws(), data = stanData_classic, parallel_chains = n_chains)

	simulated_dbh_ssm = stanData_ssm$sd_dbh*generate_quantities_ssm$draws("current_dbh")
	simulated_dbh_classic = stanData_classic$sd_dbh*generate_quantities_classic$draws("current_dbh")
	simulated_dbh_classic_avg = stanData_classic$sd_dbh*generate_quantities_classic$draws("final_dbh")

	simulatedGrowth_avg_ssm = generate_quantities_ssm$draws("simulatedGrowth_avg")
	simulatedGrowth_avg_classic = generate_quantities_classic$draws("simulatedGrowth_avg")
	simulatedGrowth_avg_clim_avg = generate_quantities_classic$draws("simulatedGrowth_avg_clim_avg")

	# --- Compute the 2.5 and 97.5 quantiles due to uncertainty on parameters (no process error!)
	simulatedGrowth_avg_ssm_qt = stanData_ssm$sd_dbh * apply(X = simulatedGrowth_avg_ssm, FUN = quantile, MARGIN = 3,
		probs = c(0.025, 0.5, 0.975))

	simulatedGrowth_avg_classic_qt = stanData_ssm$sd_dbh *
		apply(X = simulatedGrowth_avg_classic, FUN = quantile, MARGIN = 3, probs = c(0.025, 0.5, 0.975))
	
	simulatedGrowth_avg_clim_avg_qt = stanData_ssm$sd_dbh/nbYearsGrowth_new *
		t(apply(X = simulatedGrowth_avg_clim_avg, FUN = quantile, MARGIN = 3, probs = c(0.025, 0.5, 0.975))) # transpose is important!

	simulatedGrowth_avg_clim_avg = stanData_ssm$sd_dbh/nbYearsGrowth_new * apply(X = simulatedGrowth_avg_clim_avg, FUN = mean, MARGIN = 3)

	# --- Reconstruct
	simulatedGrowth_avg_ssm = stanData_ssm$sd_dbh * apply(X = simulatedGrowth_avg_ssm, FUN = mean, MARGIN = 3)
	simulatedGrowth_avg_classic = stanData_ssm$sd_dbh * apply(X = simulatedGrowth_avg_classic, FUN = mean, MARGIN = 3)
	
	growth_dt_ssm = data.table(growth = simulatedGrowth_avg_ssm, year = numeric(stanData_ssm$n_growing_years_new))
	growth_dt_classic = data.table(growth = simulatedGrowth_avg_classic, year = numeric(stanData_classic$n_growing_years_new))
	count_start = 1
	for (i in 1:stanData_ssm$n_indiv_new) # stanData_classic$n_indiv_new is the same
	{
		count_end = count_start + nbYearsGrowth_new[i] - 1
		growth_dt_ssm[count_start:count_end, year := year_start_indiv[i]:(year_start_indiv[i] + nbYearsGrowth_new[i] - 1)]
		growth_dt_classic[count_start:count_end, year := year_start_indiv[i]:(year_start_indiv[i] + nbYearsGrowth_new[i] - 1)]
		count_start = count_end + 1
	}

	growth_dt_ssm = growth_dt_ssm[(year_start <= year) & (year <= year_end_growth)]
	growth_dt_classic = growth_dt_classic[(year_start <= year) & (year <= year_end_growth)]

	simulatedGrowth_avg_ssm_qt = growth_dt_ssm[, quantile(growth, probs = c(0.025, 0.5, 0.975)), by = year]
	simulatedGrowth_avg_classic_qt = growth_dt_classic[, quantile(growth, probs = c(0.025, 0.5, 0.975)), by = year]

	simulatedGrowth_avg_ssm_qt[, type := "q50"]
	simulatedGrowth_avg_ssm_qt[simulatedGrowth_avg_ssm_qt[, .I[which.min(V1)], by = .(year)][, V1], type := "q025"]
	simulatedGrowth_avg_ssm_qt[simulatedGrowth_avg_ssm_qt[, .I[which.max(V1)], by = .(year)][, V1], type := "q975"]
	
	simulatedGrowth_avg_classic_qt[, type := "q50"]
	simulatedGrowth_avg_classic_qt[simulatedGrowth_avg_classic_qt[, .I[which.min(V1)], by = .(year)][, V1], type := "q025"]
	simulatedGrowth_avg_classic_qt[simulatedGrowth_avg_classic_qt[, .I[which.max(V1)], by = .(year)][, V1], type := "q975"]

	setkey(simulatedGrowth_avg_ssm_qt, type, year) # Ordered by type and year
	setkey(simulatedGrowth_avg_classic_qt, type, year) # Ordered by type and year
	
	growth_dt_ssm_avg = growth_dt_ssm[, mean(growth), by = year][, V1]
	growth_dt_classic_avg = growth_dt_classic[, mean(growth), by = year][, V1]

	# Plot
	if (!is.null(extension))
	{
		for (currentExt in extension)
		{			
			filename = paste0(tree_path, "ssm_approach_time_series.", currentExt)
			if (currentExt == "pdf")
				pdf(filename, height = 10, width = 10)
			if (currentExt == "tex")
			{
				tikz(filename, height = 1.2, width = 3.883282)
				op = par(mar = c(2.5, 2.5, 0.8, 0.8), mgp = c(1.5, 0.3, 0), tck = -0.015)
			}
			plot(year_start:year_end_growth, growth_dt_ssm_avg, type = "l", xlab = "Year", ylab = "Growth (mm/yr)", lwd = 2, col = "#E9851D",
				ylim = range(simulatedGrowth_avg_ssm_qt[, V1]))
			polygon(c(rev(year_start:year_end_growth), year_start:year_end_growth),
				c(rev(simulatedGrowth_avg_ssm_qt["q025", V1]), simulatedGrowth_avg_ssm_qt["q975", V1]), col = "#E9851D66", border = NA)
			if (caption)
				legend(x = "topleft", legend = "SSM", fill = "#E9851D", box.lwd = 0)
			dev.off()

			filename = paste0(tree_path, "classic_approach_time_series.", currentExt)
			if (currentExt == "pdf")
				pdf(filename, height = 10, width = 10)
			if (currentExt == "tex")
			{
				tikz(filename, height = 1.2, width = 3.883282)
				op = par(mar = c(2.5, 2.5, 0.8, 0.8), mgp = c(1.5, 0.3, 0), tck = -0.015)
			}
			plot(year_start:year_end_growth, growth_dt_classic_avg, type = "l", xlab = "Year", ylab = "Growth (mm/yr)", lwd = 2, col = "#2E77AB",
				ylim = range(simulatedGrowth_avg_classic_qt[, V1]))
			polygon(c(rev(year_start:year_end_growth), year_start:year_end_growth),
				c(rev(simulatedGrowth_avg_classic_qt["q025", V1]), simulatedGrowth_avg_classic_qt["q975", V1]),
				col = "#2E77AB66", border = NA)
			abline(h = mean(simulatedGrowth_avg_clim_avg), lwd = 2, lty = 2)
			if (caption)
				legend(x = "topleft", legend = "Classic", fill = "#2E77AB", box.lwd = 0)
			dev.off()
		}
	} else {
		# --- SSM
		plot(year_start:year_end_growth, growth_dt_ssm_avg, type = "l", xlab = "Year", ylab = "Growth (mm/yr)", lwd = 2, col = "#E9851D",
			ylim = range(simulatedGrowth_avg_ssm_qt[, V1]))
		polygon(c(rev(year_start:year_end_growth), year_start:year_end_growth),
			c(rev(simulatedGrowth_avg_ssm_qt["q025", V1]), simulatedGrowth_avg_ssm_qt["q975", V1]), col = "#E9851D66", border = NA)
		if (caption)
			legend(x = "topleft", legend = "SSM", fill = "#E9851D", box.lwd = 0)

		# --- Classic
		plot(year_start:year_end_growth, growth_dt_classic_avg, type = "l", xlab = "Year", ylab = "Growth (mm/yr)", lwd = 2, col = "#2E77AB",
			ylim = range(simulatedGrowth_avg_classic_qt[, V1]))
		polygon(c(rev(year_start:year_end_growth), year_start:year_end_growth),
			c(rev(simulatedGrowth_avg_classic_qt["q025", V1]), simulatedGrowth_avg_classic_qt["q975", V1]),
			col = "#2E77AB66", border = NA)
		abline(h = mean(simulatedGrowth_avg_clim_avg), lwd = 2, lty = 2)
		if (caption)
			legend(x = "topleft", legend = "Classic", fill = "#2E77AB", box.lwd = 0)
	}

	return (list(simulated_dbh_ssm = simulated_dbh_ssm, simulated_dbh_classic = simulated_dbh_classic,
		simulated_dbh_classic_avg = simulated_dbh_classic_avg, years = year_start:(year_end + 1)))
}

## Function to plot growth vs dbh with the uncertainty related to parameters estimation (minus process error)
plotGrowth_dbh = function(species, run, ls_info, caption = TRUE, extension = NULL, label_axis = FALSE, span = NULL, ...)
{
	# Local function to format data
	formatNewData = function(tree_path, run, n_dbh_new, lower_dbh, upper_dbh)
	{
		stanData_ssm = readRDS(paste0(tree_path, run, "_stanData.rds"))
		stanData_classic = readRDS(paste0(tree_path, run, "_stanData_classic.rds"))

		n_climate_new = 1

		# --- Species-specific averages (scaled)
		pr_rep = 0
		tas_rep = 0
		ph_rep = 0
		ba_rep = 0

		stanData_ssm$x_r = c(pr_rep, tas_rep, ph_rep, ba_rep)
		stanData_classic$x_r = c(pr_rep, tas_rep, ph_rep, ba_rep)

		stanData_ssm$n_climate_new = 1
		stanData_ssm$n_dbh_new = n_dbh_new
		stanData_ssm$n_growth = stanData_ssm$n_latentGrowth
		stanData_ssm$lower_bound = unname(lower_dbh)
		stanData_ssm$upper_bound = unname(upper_dbh)

		stanData_classic$n_climate_new = 1
		stanData_classic$n_dbh_new = n_dbh_new
		stanData_classic$lower_bound = unname(lower_dbh)
		stanData_classic$upper_bound = unname(upper_dbh)

		return(list(stanData_ssm = stanData_ssm, stanData_classic = stanData_classic, dbh = seq(lower_dbh, upper_dbh, length.out = n_dbh_new)))
	}

	# Paths and check-up
	tree_path = paste0("./", species, "/")
	if (!dir.exists(tree_path))
		stop(paste0("Path not found for species <", species, ">."))

	if (!is.null(extension))
	{
		if (!all(extension %in% c("pdf", "tex")))
			stop("Extension can only be pdf or tex")
	}

	# Info species to get parametrisation range 
	speciesInfo = ls_info[["info"]]
	speciesInfo_runs = ls_info[["range_subset"]]

	# Results
	# --- State-Space Model approach
	info_lastRun = getLastRun(path = tree_path, begin = "growth-", extension = "_main.rds$", run = run)
	lastRun = info_lastRun[["file"]]
	ssm = readRDS(paste0(tree_path, lastRun))

	# --- Classic approach
	info_lastRun = getLastRun(path = tree_path, begin = "growth-", extension = "_classic.rds$", run = run)
	lastRun = info_lastRun[["file"]]
	classic = readRDS(paste0(tree_path, lastRun))

	# Simulate growth for many dbh, environment set to 0 (i.e., average)
	# --- Stan models to simulate growth
	gq_model_ssm = cmdstan_model("generate_growth.stan")
	gq_model_classic = cmdstan_model("generate_growth_classic.stan")

	# --- Diameter space
	providedArgs = list(...)
	nbArgs = length(providedArgs)
	ls_names = names(providedArgs)

	dbh_ind = stri_detect(str = ls_names, regex = "^dbh_m.*")
	if (any(dbh_ind))
	{
		if (!all(c("dbh_min", "dbh_max") %in% ls_names[dbh_ind]))
			stop("If you want to provide informations on diameters, please provide both dbh_min and dbh_max")
		lower_dbh = providedArgs[["dbh_min"]]
		upper_dbh = providedArgs[["dbh_max"]]
	} else {
		varMin = paste0("dbh_025_", run)
		varMax = paste0("dbh_975_", run)
		lower_dbh = unname(unlist(speciesInfo_runs[species, ..varMin]))
		upper_dbh = unname(unlist(speciesInfo_runs[species, ..varMax]))
	}
	n_dbh_new = round(unname(round(upper_dbh - lower_dbh) + 1)/5) # To force a delta_dbh of around 5 mm

	# Simulate growth along the environmental gradient for many dbh
	# --- Common variables
	n_chains = ssm$num_chains() # Same for classic approach
	stanData = formatNewData(tree_path, run, n_dbh_new, lower_dbh, upper_dbh)

	# --- Generate quantities
	generate_quantities_ssm = gq_model_ssm$generate_quantities(ssm$draws(), data = stanData[["stanData_ssm"]], parallel_chains = n_chains)
	generate_quantities_classic = gq_model_classic$generate_quantities(classic$draws(), data = stanData[["stanData_classic"]],
		parallel_chains = n_chains)

	growth_ssm = stanData[["stanData_ssm"]]$sd_dbh*generate_quantities_ssm$draws("simulatedGrowth_avg")
	growth_classic = stanData[["stanData_classic"]]$sd_dbh*generate_quantities_classic$draws("simulatedGrowth_avg")

	# --- Compute the 5 and 95 quantiles due to uncertainty on parameters (no process error!)
	growth_q5_ssm = apply(X = growth_ssm, FUN = quantile, MARGIN = 3, probs = 0.05)
	growth_q95_ssm = apply(X = growth_ssm, FUN = quantile, MARGIN = 3, probs = 0.95)
	growth_ssm = apply(X = growth_ssm, FUN = mean, MARGIN = 3)

	growth_q5_classic = apply(X = growth_classic, FUN = quantile, MARGIN = 3, probs = 0.05)
	growth_q95_classic = apply(X = growth_classic, FUN = quantile, MARGIN = 3, probs = 0.95)
	growth_classic = apply(X = growth_classic, FUN = mean, MARGIN = 3)

	# --- plot
	if (label_axis)
	{
		xlab = "Diameter (mm)"
		ylab = "Growth (mm/year)"
	} else {
		xlab = ""
		ylab = ""
	}

	if (!is.null(extension))
	{
		for (currentExt in extension)
		{
			filename = paste0(tree_path, "ssm-vs-classic_approach_dbh_", species, ".", currentExt)
			if (currentExt == "pdf")
				pdf(filename, height = 10, width = 10)
			if (currentExt == "tex")
				tikz(filename, height = 3, width = 3)
			plot(stanData[["dbh"]], growth_ssm, xlab = xlab, ylab = ylab, col = "#E9851D", type = "l",
				lwd = 2, lty = 1, las = 1, xlim = span, ylim = c(0, max(c(growth_q95_ssm, growth_q95_classic))))
			lines(stanData[["dbh"]], growth_classic, col = "#2E77AB", lwd = 2, lty = 2)
			polygon(c(rev(stanData[["dbh"]]), stanData[["dbh"]]), c(rev(growth_q5_ssm), growth_q95_ssm),
				col = "#E9851D66", border = NA)
			polygon(c(rev(stanData[["dbh"]]), stanData[["dbh"]]), c(rev(growth_q5_classic), growth_q95_classic),
				col = "#2E77AB66", border = NA)
			if (caption)
				legend(x = "topright", legend = c("SSM", "Classic"), fill = c("#E9851D", "#2E77AB"), box.lwd = 0)
			dev.off()
		}
	} else {
		plot(stanData[["dbh"]], growth_ssm, xlab = xlab, ylab = ylab, col = "#E9851D", type = "l",
			lwd = 2, lty = 1, las = 1, xlim = span, ylim = c(0, max(c(growth_q95_ssm, growth_q95_classic))))
		lines(stanData[["dbh"]], growth_classic, col = "#2E77AB", lwd = 2, lty = 2)
		polygon(c(rev(stanData[["dbh"]]), stanData[["dbh"]]), c(rev(growth_q5_ssm), growth_q95_ssm),
			col = "#E9851D66", border = NA)
		polygon(c(rev(stanData[["dbh"]]), stanData[["dbh"]]), c(rev(growth_q5_classic), growth_q95_classic),
			col = "#2E77AB66", border = NA)
		if (caption)
			legend(x = "topright", legend = c("SSM", "Classic"), fill = c("#E9851D", "#2E77AB"), box.lwd = 0)
		dev.off()
	}
}

## Function to print the parameters in a latex table
printParams = function(ls_species, params_names, run = 1)
{
	ssm_params = vector(mode = "list", length = length(ls_species))
	names(ssm_params) = ls_species
	classic_params = vector(mode = "list", length = length(ls_species))
	names(classic_params) = ls_species

	for (species in ls_species)
	{
		# Paths and check-up
		tree_path = paste0("./", species, "/")
		if (!dir.exists(tree_path))
			stop(paste0("Path not found for species <", species, ">."))

		# Results
		# --- State-Space Model approach
		info_lastRun = getLastRun(path = tree_path, begin = "growth-", extension = "_main.rds$", run = run)
		ssm = readRDS(paste0(tree_path, info_lastRun[["file"]]))

		# --- Classic approach
		info_lastRun = getLastRun(path = tree_path, begin = "growth-", extension = "_classic.rds$", run = run)
		classic = readRDS(paste0(tree_path, info_lastRun[["file"]]))

		ssm_params[[species]] = getParams(model_cmdstan = ssm, params_names = params_names, type = "quantile")
		classic_params[[species]] = getParams(model_cmdstan = classic, params_names = params_names, type = "quantile")
	}
	ssm_params = rbindlist(l = ssm_params, idcol = "speciesName_sci")
	classic_params = rbindlist(l = classic_params, idcol = "speciesName_sci")

	return (list(ssm_params = ssm_params, classic_params = classic_params))
}

## Function to get the parameters in a "gaussian style", i.e., rewrite growth as exp[-1/(2σ^2) (x - μ)^2]
gaussStyle = function(params)
{
	intercept = params["averageGrowth"]

	dbh_slope = params["dbh_slope"]
	dbh_slope2 = params["dbh_slope2"]
	
	pr_slope = params["pr_slope"]
	pr_slope2 = params["pr_slope2"]
	
	tas_slope = params["tas_slope"]
	tas_slope2 = params["tas_slope2"]
	
	ph_slope = params["ph_slope"]
	ph_slope2 = params["ph_slope2"]

	competition_slope = params["competition_slope"]
	
	mu = c(mu_dbh = NA, mu_pr = NA, mu_tas = NA, mu_ph = NA)
	sigma = c(sigma_dbh = NA, sigma_pr = NA, sigma_tas = NA, sigma_ph = NA)

	# New dbh coefficients
	if (dbh_slope2 < 0)
	{
		mu["mu_dbh"] = - dbh_slope/(2*dbh_slope2)
		sigma["sigma_dbh"] = - 1/(2*dbh_slope2)
	}

	# New precipitation coefficients
	if (pr_slope2 < 0)
	{
		mu["mu_pr"] = - pr_slope/(2*pr_slope2)
		sigma["sigma_pr"] = - 1/(2*pr_slope2)
	}

	# New temperature coefficients
	if (tas_slope2 < 0)
	{
		mu["mu_tas"] = - tas_slope/(2*tas_slope2)
		sigma["sigma_tas"] = - 1/(2*tas_slope2)
	}

	# New pH coefficients
	if (ph_slope2 < 0)
	{
		mu["mu_ph"] = - ph_slope/(2*ph_slope2)
		sigma["sigma_ph"] = - 1/(2*ph_slope2)
	}

	# New intercept (formula tested analytically)
	intercept = intercept +
		ifelse(dbh_slope2 < 0, 0.5*mu["mu_dbh"]^2/sigma["sigma_dbh"], 0) +
		ifelse(pr_slope2 < 0, 0.5*mu["mu_pr"]^2/sigma["sigma_pr"], 0) +
		ifelse(tas_slope2 < 0, 0.5*mu["mu_tas"]^2/sigma["sigma_tas"], 0) +
		ifelse(ph_slope2 < 0, 0.5*mu["mu_ph"]^2/sigma["sigma_ph"], 0)

	return (list(mu = mu, sigma = sigma, intercept = intercept))
}



## Extract climate for a given species
extractClimate = function(...)
{
	# Get list of arguments
	providedArgs = list(...)
	ls_names = names(providedArgs)

	isConform = FALSE

	climate_activated = FALSE
	indices_activated = FALSE
	ph_activated = FALSE
	ph_alone = FALSE
	
	if ("indices" %in% ls_names)
		indices_activated = TRUE

	if ("climate" %in% ls_names)
	{
		climate_activated = TRUE
		climate = providedArgs[["climate"]]
	}

	if ("ph" %in% ls_names)
	{
		ph_activated = TRUE
		ph = providedArgs[["ph"]]
	}
	
	if (indices_activated)
	{
		indices = providedArgs[["indices"]]

		indices = unique(indices[, .(plot_id, index_clim_start, index_clim_end)])
		indices[, nbYears := index_clim_end - index_clim_start + 1]
		tot_nbYears = indices[, sum(nbYears)]

		extract_indices = integer(tot_nbYears)
		end = 0
		for (i in 1:indices[, .N])
		{
			start = end + 1
			end = start + indices[i, nbYears] - 1
			extract_indices[start:end] = indices[i, index_clim_start]:indices[i, index_clim_end]
		}

		if (climate_activated & ph_activated)
			predictors = data.table::merge.data.table(climate, ph, by = "plot_id")

		if (climate_activated & !ph_activated)
			predictors = climate

		if (!climate_activated & ph_activated)
		{
			ph = data.table::merge.data.table(ph, indices[, .(plot_id, nbYears)], by = "plot_id")
			predictors = data.table(plot_id = rep(ph[, plot_id], times = ph[, nbYears]), ph = rep(ph[, ph], times = ph[, nbYears]))
			ph_alone = TRUE
		}

		if (climate_activated | ph_activated)
			isConform = TRUE
	}

	if (all(c("path", "run") %in% ls_names))
	{
		print(paste("Using path and run. This extract climate for subsample", run))
		
		path = providedArgs[["path"]]
		run = providedArgs[["run"]]

		indices = readRDS(paste0(path, run, "_indices.rds"))
		stanData = readRDS(paste0(path, run, "_stanData.rds"))
		
		precip_name = names(stanData)[stringi::stri_detect_regex(names(stanData), "pr")]
		precip_name = precip_name[!stringi::stri_detect_regex(precip_name, "_mu")]
		precip_name = precip_name[!stringi::stri_detect_regex(precip_name, "_sd")]

		temperature_name = names(stanData)[stringi::stri_detect_regex(names(stanData), "tas")]
		temperature_name = temperature_name[!stringi::stri_detect_regex(temperature_name, "_mu")]
		temperature_name = temperature_name[!stringi::stri_detect_regex(temperature_name, "_sd")]

		ph_name = names(stanData)[stringi::stri_detect_regex(names(stanData), "ph")]
		ph_name = ph_name[!stringi::stri_detect_regex(ph_name, "_mu")]
		ph_name = ph_name[!stringi::stri_detect_regex(ph_name, "_sd")]

		predictors = data.table::data.table(temp1 = stanData[[precip_name]], temp2 = stanData[[temperature_name]],
			temp3 = stanData[[ph_name]])
		data.table::setnames(predictors, new = c(precip_name, temperature_name, ph_name))

		indices = unique(indices[, .(plot_id, index_clim_start, index_clim_end)])
		indices[, nbYears := index_clim_end - index_clim_start + 1]
		tot_nbYears = indices[, sum(nbYears)]

		extract_indices = integer(tot_nbYears)
		end = 0
		for (i in 1:indices[, .N])
		{
			start = end + 1
			end = start + indices[i, nbYears] - 1
			extract_indices[start:end] = indices[i, index_clim_start]:indices[i, index_clim_end]
		}

		isConform = TRUE
	}

	if (all(c("path_ind", "path_clim", "path_ph", "run") %in% ls_names))
	{
		print(paste("Using path for index and climate, and run. This extract climate for subsample", run))
		
		path_ind = providedArgs[["path_ind"]]
		path_clim = providedArgs[["path_clim"]]
		path_ph = providedArgs[["path_ph"]]
		run = providedArgs[["run"]]

		indices = readRDS(paste0(path_ind, run, "_indices.rds"))
		climate = readRDS(paste0(path_clim, "europe_reshaped_climate.rds"))
		ph = readRDS(paste0(path_clim, "europe_reshaped_soil.rds"))

		indices = unique(indices[, .(plot_id, index_clim_start, index_clim_end)])
		indices[, nbYears := index_clim_end - index_clim_start + 1]
		tot_nbYears = indices[, sum(nbYears)]

		extract_indices = integer(tot_nbYears)
		end = 0
		for (i in 1:indices[, .N])
		{
			start = end + 1
			end = start + indices[i, nbYears] - 1
			extract_indices[start:end] = indices[i, index_clim_start]:indices[i, index_clim_end]
		}

		predictors = data.table::merge.data.table(climate, ph, by = "plot_id")
		
		isConform = TRUE
	}

	if (!isConform)
		stop("You must provide climate/indices together or path/run together")

	return (list(extract_indices = extract_indices, extracted_predictors = if (ph_alone) predictors else predictors[extract_indices]))
}
